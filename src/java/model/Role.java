package model;

/**
 *
 * @author admin
 */
public class Role {
    private String id;
    private String name;
    private boolean status;

    public Role() {
    }

    public Role(String id) {
        this.id = id;
    }

    public Role(String id, String name, boolean status) {
        this.id = id;
        this.name = name;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public boolean isStatus() {
        return status;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
    
}
